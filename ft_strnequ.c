#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	unsigned int	a;

	if (s1 && s2)
	{
		a = 0;
		while (((s1[a] && s2[a]) && a <= n) && s1[a] == s2[a])
			a++;
		if (s1[a] != '\0' || s2[a] != '\0')
			return (0);
		else
			return (1);
	}
	return (0);
}
